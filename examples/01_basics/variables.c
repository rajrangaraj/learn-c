#include <stdio.h>

int main() {
    // Integer variables
    int age = 25;
    char grade = 'A';
    
    // Floating point variables
    float height = 1.75;
    double pi = 3.14159265359;
    
    // Printing different types
    printf("Age: %d\n", age);
    printf("Grade: %c\n", grade);
    printf("Height: %.2f meters\n", height);
    printf("Pi: %.10lf\n", pi);
    
    return 0;
} 